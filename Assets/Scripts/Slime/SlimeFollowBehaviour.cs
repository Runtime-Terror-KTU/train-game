using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class SlimeFollowBehaviour : StateMachineBehaviour
{
    private Transform playerPos;
    public float speed;
    public float attackRange;

    Vector3 myPosition;
    Vector3 playerPosition;
    // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        playerPos = GameObject.FindGameObjectWithTag("Player").transform;
    }

    // OnStateUpdate is called on each Update frame between OnStateEnter and OnStateExit callbacks
    override public void OnStateUpdate(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        animator.transform.LookAt(playerPos);
        animator.transform.position = Vector3.MoveTowards(animator.transform.position, playerPos.position, speed * Time.deltaTime);

        playerPosition = GameObject.FindGameObjectWithTag("Player").transform.position;
        myPosition = animator.transform.position;

        if (Vector3.Distance(myPosition, playerPosition) < attackRange)
        {
            animator.SetTrigger("Attack");
        }
    }

    // OnStateExit is called when a transition ends and the state machine finishes evaluating this state
    override public void OnStateExit(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        animator.ResetTrigger("Attack");
    }

